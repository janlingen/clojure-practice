(comment "Write a function which multiplies two numbers
and returns the result as a sequence of its digits.")


(defn solution
  [& args])


(assert (and (= (solution 1 1) [1])
             (= (solution 99 9) [8 9 1])
             (= (solution 999 99) [9 8 9 0 1])))
